        const document: PdfDocument = new PdfDocument();
        const page: PdfPage = document.addPage();
        const form: PdfForm = document.form;
        const dateField: PdfTextBoxField = new PdfTextBoxField(page, 'dateInput', {
            x: 50, y: 120, width: 150, height: 20
        });
        dateField.text = "18/01/2003";
        const formatScript = `
    // If the value is 8 digits (ddMMyyyy), insert slashes => dd/MM/yyyy
    if (event.value && /^\\d{8}$/.test(event.value)) {
      event.value = event.value.replace(/(\\d{2})(\\d{2})(\\d{4})/, '$1/$2/$3');
    }
  `;
        const formatAction = new PdfJavaScriptAction(formatScript);
        dateField.actions.format = formatAction;
        expect((dateField as any)._dictionary.has('AA')).toBeTruthy();
        const aa = (dateField as any)._dictionary.get('AA');
        expect(aa).toBeDefined();
        expect(aa.has('F')).toBeTruthy();
        expect(aa.size).toEqual(1);
        expect(dateField.actions.format.script).toContain('event.value');
        form.add(dateField);
        const bytes = document.save();
        document.destroy();
        const parsed = new PdfDocument(bytes);
        const parsedField = parsed.form.fieldAt(0) as PdfTextBoxField;
        expect(parsedField.actions.format).toBeDefined();
        expect(parsedField.actions.format.script).toContain('event.value');
        const parsedAA = (parsedField as any)._dictionary.get('AA');
        expect(parsedField.getValue('V')).toBe("18/01/2003");
        expect(parsedAA).toBeDefined();
        expect(parsedAA.has('F')).toBeTruthy();
        const parsedFormat = parsedAA.get('F');
        expect(parsedFormat).toBeDefined();
        expect(parsedAA.size).toBe(1);
        parsed.destroy();
